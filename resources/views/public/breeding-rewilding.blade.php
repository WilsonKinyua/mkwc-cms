                <h3>Mountain Bongo Rewilding</h3>
                <p>The long-term vision of the Government of Kenya's National Recovery and Action Plan (2019-2023) for the
                    Mountain Bongo is to achieve a population of 750 individuals in Kenya by the year 2050. The opening of
                    the Sanctuary represents a significant step towards achieving this vision.

                </p>
                <p>In proud partnership with Kenya Wildlife Service (KWS) and Kenya Forest Service (KFS), Mount Kenya
                    Wildlife Conservancy has led a breeding and rewilding programme that has taken nearly 20 years to bring
                    the Mountain Bongo back into the wild. The Mawingu Mountain Bongo Sanctuary, a 776-acre pristine natural
                    forest set at the foothills of Mount Kenya, provides a more extensive area to continue rewilding the
                    Mountain Bongo in preparation for survival in the wild. It is the first-ever Mountain Bongo Sanctuary in
                    Africa and the world.

                </p>
                <p>With the Sanctuary now operational in 2022, Mount Kenya Wildlife Conservancy, KWS and partners will have
                    facilities to complete Mountain Bongo reintroduction end-to-end. This implies that breeding and
                    rewilding of the Mountain Bongo will continue to be done concurrently, thus creating a stream of
                    candidates to reinforce wild bongo populations and reintroduce them into the historical ranges.

                </p>
                <p>The first five Mountain Bongos released into the Sanctuary were already primed for that environment.
                    Every subsequent year, an additional 10 Mountain Bongos will be translocated into the Sanctuary in
                    groups of 5 every six months. Individual Mountain Bongos to join the Sanctuary will be selected
                    offspring from the breeding herds and allowed to roam and mate randomly. We estimate that by 2025, the
                    Sanctuary will have 50-70 fully rewilded Mountain Bongos.

                </p>
                <p>The aim of the Mountain Bongo breeding and the rewilding programme is to have individuals that are fully
                    rewilded and therefore competent to survive in the Sanctuary and eventually the wild. Total weaning from
                    human dependence is a gradual process requiring technical skill, patience, and commitment to allow the
                    animals to adapt independently. This requires spaces with every vital component for their survival: an
                    appropriate habitat, foliage, and enough space to exhibit normal behaviour and fully accommodate natural
                    selection.

                </p>
                <p>The Mountain Bongos born in the programme, which are successfully rewilded, will be provided to KWS for
                    translocation to reinforce the wild populations and reintroduce them in the former ranges.
